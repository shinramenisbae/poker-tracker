// Post-processor: pulls the finished alias_mappings from the tracker DB,
// rewrites aliases.json to match, and rewrites dry-run.json so each player's
// name is the canonical one. Players whose alias is still unmapped are
// DROPPED from the session entirely (per "discard and ignore" — those are
// one-off appearances we can't identify).
//
// Run after friends finish using the /aliases UI, before npm run import.
import 'dotenv/config';
import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const { TRACKER_API_BASE } = process.env;
if (!TRACKER_API_BASE) { console.error('Missing TRACKER_API_BASE'); process.exit(1); }

// 1) Pull mappings
const res = await fetch(`${TRACKER_API_BASE}/alias-mappings`);
if (!res.ok) { console.error(`GET /alias-mappings failed: ${res.status}`); process.exit(1); }
const { aliases: aliasRows } = await res.json();

// Build case-insensitive lookup
const lookup = new Map();
for (const { alias, realName } of aliasRows) {
  if (realName && realName.trim()) lookup.set(alias.toLowerCase(), realName.trim());
}
console.log(`Loaded ${lookup.size} mapped aliases (of ${aliasRows.length} total).`);

// 2) Write aliases.json (so re-runs of parse.js would behave correctly too)
const aliasesJson = { _comment: 'Generated by apply-mappings.js from the tracker DB. Do not hand-edit; use the /aliases UI.' };
for (const { alias, realName } of aliasRows) {
  if (realName && realName.trim()) aliasesJson[alias] = realName.trim();
}
await fs.writeFile(path.join(__dirname, 'aliases.json'), JSON.stringify(aliasesJson, null, 2));
console.log(`Wrote aliases.json with ${Object.keys(aliasesJson).length - 1} mappings.`);

// 3) Rewrite dry-run.json
const dryRunPath = path.join(__dirname, 'dry-run.json');
const dryRun = JSON.parse(await fs.readFile(dryRunPath, 'utf8'));

let sessionsTouched = 0;
let totalDropped = 0;
let totalKept = 0;

for (const entry of dryRun) {
  if (!entry.players || entry.players.length === 0) continue;

  const dropped = [];
  const kept = [];
  for (const p of entry.players) {
    const aliasKey = (p.originalAlias || p.name || '').toLowerCase();
    const canonical = lookup.get(aliasKey);
    if (canonical) {
      kept.push({ ...p, name: canonical });
    } else {
      dropped.push(p.originalAlias || p.name);
    }
  }

  if (dropped.length > 0 || kept.length !== entry.players.length) sessionsTouched++;
  totalDropped += dropped.length;
  totalKept += kept.length;

  entry.players = kept;

  // Refresh warnings: keep non-alias warnings, replace the unmapped-alias ones
  const nonAliasWarnings = (entry.warnings || []).filter((w) => !w.startsWith('Unmapped alias'));
  if (dropped.length > 0) {
    nonAliasWarnings.push(`Dropped ${dropped.length} unmapped player(s): ${dropped.map((d) => `"${d}"`).join(', ')}`);
  }
  entry.warnings = nonAliasWarnings;

  // Re-enable willImport if it was only blocked by unmapped aliases — but keep it false
  // if it was blocked by a "Possible duplicate" warning (those need user disambiguation)
  // or if no players remain after dropping.
  if (entry.players.length === 0) {
    entry.willImport = false;
    entry.warnings.push('Session has 0 known players after dropping unmapped.');
  } else if (entry.warnings.some((w) => w.startsWith('Possible duplicate'))) {
    entry.willImport = false;
  } else if (entry.warnings.some((w) => w.startsWith('Net mismatch'))) {
    // Math inconsistencies stay flagged but don't block.
    entry.willImport = true;
  } else {
    entry.willImport = true;
  }
}

await fs.writeFile(dryRunPath, JSON.stringify(dryRun, null, 2));

const willImport = dryRun.filter((e) => e.willImport).length;
console.log();
console.log(`Sessions touched: ${sessionsTouched}`);
console.log(`Player rows kept: ${totalKept}`);
console.log(`Player rows dropped (unmapped one-offs): ${totalDropped}`);
console.log();
console.log(`dry-run.json updated. ${willImport} sessions will import, ${dryRun.length - willImport} will not.`);
console.log(`Run "npm run import" when ready.`);
